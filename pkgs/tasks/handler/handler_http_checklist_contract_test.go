// TestHTTP_patchChecklistItem_doneAgentReturnsItemsView pins the documented PATCH
// happy path for `done`: 200 + full `{ "items": [...] }` with `done:true` on the
// targeted item. This complements the existing 400 coverage in
// handler_http_checklist_test.go by locking down the success-shape side.
func TestHTTP_patchChecklistItem_doneAgentReturnsItemsView(t *testing.T) {
	srv, st := newTaskTestServerWithStore(t)
	defer srv.Close()
	taskID := mustCreateChecklistTask(t, srv, "chk-done")
	it, err := st.AddChecklistItem(context.Background(), taskID, "review", domain.ActorUser)
	if err != nil {
		t.Fatal(err)
	}

	req, err := http.NewRequest(http.MethodPatch,
		srv.URL+"/tasks/"+taskID+"/checklist/items/"+it.ID,
		strings.NewReader(`{"done":true}`))
	if err != nil {
		t.Fatal(err)
	}
	req.Header.Set("Content-Type", "application/json")
	req.Header.Set("X-Actor", "agent")
	res, err := http.DefaultClient.Do(req)
	if err != nil {
		t.Fatal(err)
	}
	defer res.Body.Close()
	raw, _ := io.ReadAll(res.Body)
	if res.StatusCode != http.StatusOK {
		t.Fatalf("status %d (want 200) body=%s", res.StatusCode, raw)
	}

	var top map[string]json.RawMessage
	if err := json.Unmarshal(raw, &top); err != nil {
		t.Fatalf("decode: %v body=%s", err, raw)
	}
	if _, ok := top["items"]; !ok || len(top) != 1 {
		t.Fatalf("PATCH 200 must return only `items`; got keys=%v body=%s", keysOf(top), raw)
	}
	var out struct {
		Items []struct {
			ID        string `json:"id"`
			SortOrder int    `json:"sort_order"`
			Text      string `json:"text"`
			Done      bool   `json:"done"`
		} `json:"items"`
	}
	if err := json.Unmarshal(raw, &out); err != nil {
		t.Fatalf("decode items: %v", err)
	}
	if len(out.Items) != 1 {
		t.Fatalf("items len=%d want 1", len(out.Items))
	}
	got := out.Items[0]
	if got.ID != it.ID || got.Text != "review" || !got.Done {
		t.Fatalf("item=%+v want id=%s text=review done=true", got, it.ID)
	}
}

