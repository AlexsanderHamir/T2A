// TestHTTP_postChecklistItem_201ResponseShape pins the documented POST 201
// envelope: the bare `domain.TaskChecklistItem` JSON `{id, task_id, sort_order, text}`.
// This is intentionally **not** the same shape as items in `GET /tasks/{id}/checklist`
// (no `done`, includes `task_id`); the doc table calls this asymmetry out so
// future struct changes that drift either way fail loudly.
func TestHTTP_postChecklistItem_201ResponseShape(t *testing.T) {
	srv := newTaskTestServer(t)
	defer srv.Close()

	taskID := mustCreateChecklistTask(t, srv, "chk-shape")

	res, err := http.Post(srv.URL+"/tasks/"+taskID+"/checklist/items", "application/json",
		strings.NewReader(`{"text":"first criterion"}`))
	if err != nil {
		t.Fatal(err)
	}
	defer res.Body.Close()
	raw, err := io.ReadAll(res.Body)
	if err != nil {
		t.Fatal(err)
	}
	if res.StatusCode != http.StatusCreated {
		t.Fatalf("status %d (want 201) body=%s", res.StatusCode, raw)
	}

	var top map[string]json.RawMessage
	if err := json.Unmarshal(raw, &top); err != nil {
		t.Fatalf("decode: %v body=%s", err, raw)
	}
	wantKeys := map[string]struct{}{"id": {}, "task_id": {}, "sort_order": {}, "text": {}}
	for k := range wantKeys {
		if _, ok := top[k]; !ok {
			t.Errorf("POST checklist 201 missing key %q (docs/API-HTTP.md): %s", k, raw)
		}
	}
	for k := range top {
		if _, ok := wantKeys[k]; !ok {
			t.Errorf("POST checklist 201 unexpected key %q (docs/API-HTTP.md): %s", k, raw)
		}
	}

	var row struct {
		ID        string `json:"id"`
		TaskID    string `json:"task_id"`
		SortOrder int    `json:"sort_order"`
		Text      string `json:"text"`
	}
	if err := json.Unmarshal(raw, &row); err != nil {
		t.Fatalf("decode envelope: %v", err)
	}
	if row.ID == "" {
		t.Error("id must not be empty")
	}
	if row.TaskID != taskID {
		t.Errorf("task_id=%q want %q", row.TaskID, taskID)
	}
	if row.SortOrder < 1 {
		t.Errorf("sort_order=%d want >=1", row.SortOrder)
	}
	if row.Text != "first criterion" {
		t.Errorf("text=%q want %q", row.Text, "first criterion")
	}
}

