// TestHTTP_deleteChecklistItem_publishesTaskUpdated colocates the per-route SSE
// positive invariant for DELETE: a successful removal publishes exactly
// `task_updated:{id}`.
func TestHTTP_deleteChecklistItem_publishesTaskUpdated(t *testing.T) {
	srv, st, hub := newSSETriggerServer(t)
	defer srv.Close()
	taskID := mustCreateChecklistTask(t, srv, "chk-sse-del")
	it, err := st.AddChecklistItem(context.Background(), taskID, "remove", domain.ActorUser)
	if err != nil {
		t.Fatal(err)
	}

	ch, unsub := hub.Subscribe()
	defer unsub()

	req, err := http.NewRequest(http.MethodDelete,
		srv.URL+"/tasks/"+taskID+"/checklist/items/"+it.ID, nil)
	if err != nil {
		t.Fatal(err)
	}
	res, err := http.DefaultClient.Do(req)
	if err != nil {
		t.Fatal(err)
	}
	raw, _ := io.ReadAll(res.Body)
	_ = res.Body.Close()
	if res.StatusCode != http.StatusNoContent {
		t.Fatalf("status %d (want 204) body=%s", res.StatusCode, raw)
	}

	got := summarize(drainSSE(t, ch, 1, 2*time.Second))
	mustEqualEvents(t, "DELETE /tasks/{id}/checklist/items/{itemId}", got, []string{"task_updated:" + taskID})
}

// TestHTTP_deleteChecklistItem_errorPathsNeverPublish pins the negative-side
// SSE invariant: 400 (inheriting child) and 404 (unknown task, unknown item)
// must never publish.
func TestHTTP_deleteChecklistItem_errorPathsNeverPublish(t *testing.T) {
	srv, st, hub := newSSETriggerServer(t)
	defer srv.Close()
	parentID := mustCreateChecklistTask(t, srv, "chk-sse-del-par")
	it, err := st.AddChecklistItem(context.Background(), parentID, "owned", domain.ActorUser)
	if err != nil {
		t.Fatal(err)
	}
	childID := mustCreateChildInheriting(t, srv, parentID, "chk-sse-del-child")

	ch, unsub := hub.Subscribe()
	defer unsub()

	doDelete := func(name, taskID, itemID string, want int) {
		t.Helper()
		req, err := http.NewRequest(http.MethodDelete,
			srv.URL+"/tasks/"+taskID+"/checklist/items/"+itemID, nil)
		if err != nil {
			t.Fatal(err)
		}
		res, err := http.DefaultClient.Do(req)
		if err != nil {
			t.Fatalf("%s: %v", name, err)
		}
		raw, _ := io.ReadAll(res.Body)
		_ = res.Body.Close()
		if res.StatusCode != want {
			t.Fatalf("%s: status %d (want %d) body=%s", name, res.StatusCode, want, raw)
		}
	}

	doDelete("inheritingChild", childID, it.ID, http.StatusBadRequest)
	doDelete("unknownTask", "11111111-1111-4111-8111-111111111111", it.ID, http.StatusNotFound)
	doDelete("unknownItem", parentID, "22222222-2222-4222-8222-222222222222", http.StatusNotFound)

	got := summarize(drainSSE(t, ch, 0, 200*time.Millisecond))
	if len(got) != 0 {
		t.Fatalf("drained SSE events %v after DELETE checklist error round-trips; want zero (400/404 paths must never publish)", got)
	}
}

func mustCreateChecklistTask(t *testing.T, srv *httptest.Server, title string) string {
	t.Helper()
	res, err := http.Post(srv.URL+"/tasks", "application/json",
		strings.NewReader(`{"title":"`+title+`","priority":"medium"}`))
	if err != nil {
		t.Fatal(err)
	}
	defer res.Body.Close()
	body, _ := io.ReadAll(res.Body)
	if res.StatusCode != http.StatusCreated {
		t.Fatalf("create task status %d body=%s", res.StatusCode, body)
	}
	var task domain.Task
	if err := json.Unmarshal(body, &task); err != nil {
		t.Fatal(err)
	}
	return task.ID
}

func mustCreateChildInheriting(t *testing.T, srv *httptest.Server, parentID, title string) string {
	t.Helper()
	body := `{"title":"` + title + `","priority":"medium","parent_id":"` + parentID + `","checklist_inherit":true}`
	res, err := http.Post(srv.URL+"/tasks", "application/json", strings.NewReader(body))
	if err != nil {
		t.Fatal(err)
	}
	defer res.Body.Close()
	raw, _ := io.ReadAll(res.Body)
	if res.StatusCode != http.StatusCreated {
		t.Fatalf("create child status %d body=%s", res.StatusCode, raw)
	}
	var child domain.Task
	if err := json.Unmarshal(raw, &child); err != nil {
		t.Fatal(err)
	}
	return child.ID
}

